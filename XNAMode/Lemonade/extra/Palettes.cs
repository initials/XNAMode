using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using org.flixel;
using Microsoft.Xna.Framework;
using Microsoft.Xna.Framework.Graphics;
using Microsoft.Xna.Framework.Input;


namespace Flixel.Lemonade.extra
{
    class Palettes : FlxSprite
    {

        public Palettes(int xPos, int yPos)
            : base(xPos, yPos)
        {
            loadGraphic(FlxG.Content.Load<Texture2D>("Lemonade/"), true, false, 50, 80);

            addAnimation("animation", new int[] { 72, 73, 74, 75, 76, 77 }, 12, true);

            play("animation");
        }

        override public void update()
        {


            base.update();

        }


    }
}
